export default function SessionTab() {
    const [sessionTitle, setSessionTitle] = useState("");
    const [selectedClasses, setSelectedClasses] = useState([]);
    const [sessions, setSessions] = useState([]);
    const [showQRModal, setShowQRModal] = useState(false);
    const [generatedSession, setGeneratedSession] = useState(null);
    const [minutes, setMinutes] = useState(5);
    const [message, setMessage] = useState(null);
    const [isLoading, setIsLoading] = useState(false);
    const [isQrCodeGenLoaded] = useState(typeof window !== "undefined" && typeof window.qrcodegen !== "undefined");
    const [checkInRecords, setCheckInRecords] = useState([]);
    const [isRecordsLoading, setIsRecordsLoading] = useState(false);

    const classOptions = ["A", "B", "C", "All"];

    const showToast = (msg, type) => {
        setMessage({ text: msg, type });
    };

    const clearToast = () => setMessage(null);


    useEffect(() => {
        const loadSessions = async () => {
            try {
                const sessionsData = await fetchWithRetry(`${API_BASE_URL}/sessions`, {
                    method: "GET",
                    headers: {
                        "X-User-Id": ADMIN_USER_ID,
                    },
                });

               
                const formatted = sessionsData.map((s) => {
                    
                    const expiresAt = new Date(s.expiresAt); 
                    
                    const timeLeft = Math.max(0, Math.floor((expiresAt.getTime() - Date.now()) / 1000));
                    
                   
                    const token = s.sessionToken || s.id; 

                    return {
                        id: token,
                        title: s.sessionName,
                        eligibleClasses: s.section,
                        createdAt: new Date().toLocaleString(),
                        qrValue: token,
                  
                        duration: 0, 
                        timeLeft,
                        status: timeLeft > 0 ? "Active" : "Expired",
                    };
                });

                setSessions(formatted);
            } catch (err) {
                console.error("Failed to load sessions:", err);
                showToast("Failed to load sessions from server", "error");
            }
        };

        loadSessions();
    }, []);

    useEffect(() => {
        const interval = setInterval(() => {
            setSessions((prevSessions) =>
                prevSessions.map((s) => {
                    if (s.timeLeft > 0) {
                        return { ...s, timeLeft: s.timeLeft - 1 };
                    } else {
                       
                        return { ...s, timeLeft: 0, status: "Expired" };
                    }
                })
            );
        }, 1000);
        return () => clearInterval(interval);
    }, []);

    const formatTime = (seconds) => {
        const m = Math.floor(seconds / 60);
        const s = seconds % 60;
        return `${m.toString().padStart(2, "0")}:${s.toString().padStart(2, "0")}`;
    };

    const toggleClass = (cls) => {
        if (selectedClasses.includes(cls)) {
            setSelectedClasses(selectedClasses.filter((c) => c !== cls));
        } else {
            if (cls === "All") setSelectedClasses(["All"]);
            else setSelectedClasses(selectedClasses.filter((c) => c !== "All").concat(cls));
        }
    };

    // --- API Functions ---
    const handleCreateSession = async () => {
        if (!sessionTitle || selectedClasses.length === 0) {
            return showToast("Please fill the session title and select a class/section.", "error");
        }

        const section = selectedClasses[0] === "All" ? "All" : selectedClasses.join(",");
        setIsLoading(true);

        try {
            const tokenResponse = await fetchWithRetry(
                `${API_BASE_URL}/generate-token?section=${encodeURIComponent(section)}&sessionName=${encodeURIComponent(sessionTitle)}`,
                {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                        "X-User-Id": ADMIN_USER_ID,
                    },
                }
            );

            const expiresInMinutes = tokenResponse.expiresInMinutes || 5;
            const token = tokenResponse.token;
            const timeLeftSeconds = expiresInMinutes * 60;

            const newSession = {
                id: token,
                title: sessionTitle,
                eligibleClasses: section,
                createdAt: new Date().toLocaleString(),
                qrValue: token,
                duration: timeLeftSeconds, 
                timeLeft: timeLeftSeconds,
                status: "Active",
            };

            // Use functional update to ensure we always append to the latest state
            setSessions((prev) => [newSession, ...prev]);
            setGeneratedSession(newSession);
            setShowQRModal(true);
            showToast(`Session '${sessionTitle}' created successfully!`, "success");

            setSessionTitle("");
            setSelectedClasses([]);
        } catch (err) {
            console.error("Error creating session:", err);
            showToast("Error creating session.", "error");
        } finally {
            setIsLoading(false);
        }
    };


    const fetchSessionRecords = async (token) => {
        setIsRecordsLoading(true);
        setCheckInRecords([]);
        try {
            const records = await fetchWithRetry(`${API_BASE_URL}/attendance/${token}`, {
                method: "GET",
                headers: {
                    "X-User-Id": ADMIN_USER_ID,
                },
            });
   
            let parsed = records;
            if (typeof records === "string") {
                try {
                    parsed = JSON.parse(records);
                } catch {
                    parsed = [];
                }
            }
            setCheckInRecords(Array.isArray(parsed) ? parsed : []);
        } catch (error) {
            console.error("Failed to fetch session records:", error);
            showToast(`Error fetching records: ${error.message}`, "error");
        } finally {
            setIsRecordsLoading(false);
        }
    };

    const openQRModalWithRecords = (session) => {
        setGeneratedSession(session);
        setShowQRModal(true);
        fetchSessionRecords(session.qrValue);
    };

    const handleDownloadQR = async () => {
        const svgElement = document.getElementById("qr-svg-export");
        if (!svgElement) {
            const modal = document.querySelector('[id^="qr-svg-export"], [id="qr-svg-export"]') || document.querySelector(".qr-modal-inline svg");
            if (modal) {
         
                try {
                    const serializer = new XMLSerializer();
                    const source = serializer.serializeToString(modal);
                    const blob = new Blob([source], { type: "image/svg+xml;charset=utf-8" });
                    const url = URL.createObjectURL(blob);
                    const a = document.createElement("a");
                    a.href = url;
                    a.download = `${generatedSession.title}_${generatedSession.qrValue.substring(0, 8)}_QR.svg`;
                    document.body.appendChild(a);
                    a.click();
                    document.body.removeChild(a);
                    URL.revokeObjectURL(url);
                    showToast("QR code downloaded successfully!", "success");
                    return;
                } catch (e) {
                    console.error("Fallback download failed", e);
                }
            }

            showToast("Error: QR code SVG element not found for download.", "error");
            return;
        }

        try {
            const serializer = new XMLSerializer();
            const source = serializer.serializeToString(svgElement);
            const blob = new Blob([source], { type: "image/svg+xml;charset=utf-8" });
            const url = URL.createObjectURL(blob);
            const a = document.createElement("a");
            a.href = url;
            a.download = `${generatedSession.title}_${generatedSession.qrValue.substring(0, 8)}_QR.svg`;
            document.body.appendChild(a);
            a.click();
            document.body.removeChild(a);
            URL.revokeObjectURL(url);
            showToast("QR code downloaded successfully!", "success");
        } catch (e) {
            console.error("Download failed:", e);
            showToast("Failed to download QR code.", "error");
        }
    };

    // --- SORTING LOGIC ---
    const sortedSessions = [...sessions].sort((a, b) => {
        
        const aIsActive = a.timeLeft > 0 ? 1 : 0;
        const bIsActive = b.timeLeft > 0 ? 1 : 0;

        if (aIsActive !== bIsActive) {
            return bIsActive - aIsActive; 
        }
        
        return b.timeLeft - a.timeLeft;
    });

    return (
        <div className="bg-white rounded-xl shadow-md p-6 relative min-h-screen">
            {message && <ToastMessage message={message.text} type={message.type} onClose={clearToast} />}

            <h2 className="text-2xl font-bold mb-6 text-gray-800 border-b pb-2">Create New Session</h2>

            <div className="space-y-4">
                <input
                    type="text"
                    placeholder="Enter session title (e.g., Physics Lecture 3)"
                    className="w-full border border-gray-300 px-4 py-3 rounded-xl focus:ring-blue-500 focus:border-blue-500 transition"
                    value={sessionTitle}
                    onChange={(e) => setSessionTitle(e.target.value)}
                />

                <div>
                    <p className="font-medium mb-2 text-gray-700">Select Eligible Classes:</p>
                    <div className="flex flex-wrap gap-3">
                        {classOptions.map((cls) => (
                            <button
                                key={cls}
                                onClick={() => toggleClass(cls)}
                                className={`px-4 py-2 rounded-full border-2 transition duration-200 shadow-sm
                                    ${selectedClasses.includes(cls)
                                    ? "bg-blue-600 text-white border-blue-600 hover:bg-blue-700 shadow-lg"
                                    : "bg-gray-100 text-gray-700 border-gray-300 hover:bg-gray-200"
                                    }`}
                            >
                                {cls}
                            </button>
                        ))}
                    </div>
                </div>

                <div className="flex gap-4 p-3 bg-gray-50 rounded-xl border border-gray-200">
                    <div className="flex-1">
                        <label className="block text-sm font-medium text-gray-700 mb-1">Duration (Minutes)</label>
                        <input
                            type="number"
                            min="1"
                            max="60"
                            placeholder="5"
                            value={minutes}
                            onChange={(e) => setMinutes(Number(e.target.value))}
                            className="w-full border border-gray-300 px-4 py-2 rounded-xl"
                        />
                    </div>
                </div>

                <button
                    onClick={handleCreateSession}
                    disabled={isLoading}
                    className={`w-full text-white px-5 py-3 rounded-xl transition duration-300 font-semibold shadow-lg ${
                        isLoading ? "bg-gray-400 cursor-not-allowed" : "bg-green-600 hover:bg-green-700"
                    }`}
                >
                    {isLoading ? (
                        <span className="flex items-center justify-center">
                            <svg
                                className="animate-spin -ml-1 mr-3 h-5 w-5 text-white"
                                xmlns="http://www.w3.org/2000/svg"
                                fill="none"
                                viewBox="0 0 24 24"
                            >
                                <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
                                <path
                                    className="opacity-75"
                                    fill="currentColor"
                                    d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
                                ></path>
                            </svg>
                            Generating Token...
                        </span>
                    ) : (
                        "Generate Live QR Code Session"
                    )}
                </button>
            </div>

            <div className="mt-8 pt-4 border-t">
                <h3 className="text-xl font-bold mb-4 text-gray-800">Ongoing Sessions</h3>
                {sessions.length === 0 ? (
                    <p className="text-gray-500 p-4 border rounded-xl bg-gray-50">No currently active sessions.</p>
                ) : (
                    <div className="space-y-3">
                        {sortedSessions.map((s) => (
                            <div
                                key={s.id}
                                className={`p-4 rounded-xl shadow-md flex justify-between items-center transition 
                                    ${s.timeLeft > 0 ? "bg-white border-l-4 border-blue-500" : "bg-red-50 border border-red-300 opacity-70"}`}
                            >
                                <div>
                                    <p className="font-semibold text-gray-900">{s.title}</p>
                                    <p className="text-sm text-gray-600 mt-0.5">Eligible: {s.eligibleClasses}</p>
                                    <p className={`text-sm font-bold mt-1 ${s.timeLeft > 0 ? "text-blue-600" : "text-red-600"}`}>
                                        {s.timeLeft > 0 ? `Time left: ${formatTime(s.timeLeft)}` : "Expired"}
                                    </p>
                                </div>

                                <button
                                    onClick={() => openQRModalWithRecords(s)}
                                    className={`text-white px-4 py-2 rounded-full text-sm transition shadow-md ${
                                        s.timeLeft > 0 ? "bg-blue-500 hover:bg-blue-600" : "bg-gray-500 hover:bg-gray-600"
                                    }`}
                                >
                                    View Details
                                </button>
                            </div>
                        ))}
                    </div>
                )}
            </div>

            {/* QR Modal with Attendance Records */}
            {showQRModal && generatedSession && (
                <div className="fixed inset-0 flex items-center justify-center bg-black bg-opacity-40 z-50 p-4">
                    <div className="bg-white p-8 rounded-2xl shadow-2xl w-full max-w-4xl text-left transform translate-y-0">
                        <h3 className="text-2xl font-bold mb-2 text-gray-800">{generatedSession.title}</h3>
                        <p className="text-gray-600 mb-6">
                            Status:
                            <span className={`font-semibold ${generatedSession.timeLeft > 0 ? "text-green-600" : "text-red-600"}`}>
                                {generatedSession.timeLeft > 0 ? ` Active (${formatTime(generatedSession.timeLeft)} remaining)` : " Expired"}
                            </span>
                        </p>

                        <div className="grid grid-cols-1 md:grid-cols-2 gap-8">
                            {/* QR Code Section */}
                            <div className="flex flex-col items-center border border-gray-200 p-4 rounded-xl">
                                <h4 className="font-semibold text-lg mb-4">Live QR Code</h4>
                                <div className="mb-6 p-4 border rounded-xl bg-white qr-modal-inline">
                                    <QRCodeSVG value={generatedSession.qrValue} size={256} />
                                </div>
                                <button
                                    onClick={handleDownloadQR}
                                    className="flex items-center justify-center bg-green-500 text-white px-6 py-3 rounded-full hover:bg-green-600 transition font-semibold shadow-md w-full max-w-xs"
                                >
                                    <svg
                                        xmlns="http://www.w3.org/2000/svg"
                                        width="20"
                                        height="20"
                                        viewBox="0 0 24 24"
                                        fill="none"
                                        stroke="currentColor"
                                        strokeWidth="2"
                                        strokeLinecap="round"
                                        strokeLinejoin="round"
                                        className="mr-2"
                                    >
                                        <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4" />
                                        <polyline points="7 10 12 15 17 10" />
                                        <line x1="12" x2="12" y1="15" y2="3" />
                                    </svg>
                                    Download QR
                                </button>
                            </div>

                            {/* Attendance Records Section */}
                            <div className="border border-gray-200 p-4 rounded-xl">
                                <h4 className="font-semibold text-lg mb-4 flex justify-between items-center">
                                    Checked-In Students <span className="text-blue-600 text-sm font-bold">{checkInRecords.length} Total</span>
                                </h4>

                                {isRecordsLoading ? (
                                    <p className="text-gray-500 text-center py-8">Loading records...</p>
                                ) : checkInRecords.length === 0 ? (
                                    <p className="text-gray-500 py-8 text-center">No students have checked in yet.</p>
                                ) : (
                                    <div className="space-y-3 max-h-96 overflow-y-auto">
                                        {checkInRecords.map((record, index) => (
                                            <div key={index} className="flex justify-between items-center p-3 bg-gray-50 rounded-lg shadow-sm">
                                                <p className="font-medium text-sm truncate w-1/2">
                                                    <span className="text-gray-900 font-bold">User ID:</span> {record.userId}
                                                </p>
                                                <p className="text-xs text-gray-600 font-mono">Checked In: {new Date(record.checkInTime).toLocaleTimeString()}</p>
                                            </div>
                                        ))}
                                    </div>
                                )}
                            </div>
                        </div>

                        <button
                            onClick={() => setShowQRModal(false)}
                            className="mt-8 bg-gray-200 text-gray-800 px-6 py-3 rounded-full hover:bg-gray-300 transition font-semibold w-full"
                        >
                            Close Details
                        </button>
                    </div>
                </div>
            )}
        </div>
    );
}